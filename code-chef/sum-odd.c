// Chef has 3 numbers A,B and C.

// Chef wonders if it is possible to choose exactly two numbers out of the three numbers such
// that their sum is odd.
#include <stdio.h>
int main()
{
    int T, i = 0, a, b, c;
    scanf("%d", &T);
    for (i = 0; i < T; i++)
    {
        scanf("%d %d %d", &a, &b, &c);
    }
}